# lists are used to store a list of objects or variables
names = ["Steve", "James", "Fred"]
print(names)
# first element
print(names[0])
# last element
print(names[-1])
# second last element
print(names[-2])
# changing an index
names[0] = "Stevey"
print(names[0])
# print specific index values, returns a new list, doesn't change anything
print(names[0:3])