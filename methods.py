# methods
numbers = [1, 2, 3, 4, 5]
# inserts number to list
numbers.insert(0, 0)
print(numbers)
# removes number from list
numbers.remove(3)
print(numbers)
# adds to list
numbers.append(6)
print(numbers)
# returns length
print(len(numbers))
# clears list
numbers.clear()
print(numbers)
# returns bool
print(1 in numbers)